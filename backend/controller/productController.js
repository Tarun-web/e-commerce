const Product = require("../model/productModel");
const ErrorHandler = require("../utils/errorHandler");
const asyncErrors = require("../middleware/asyncErrors");
const ApiFeatures = require("../utils/ApiFeatures");

//create product  -- admin
exports.createProduct = asyncErrors(async (req, res, next) => {
  const product = await Product.create(req.body);

  res.status(201).json({
    success: true,
    product,
  });
});

//get all products
exports.getAllProducts = asyncErrors(async (req, res, next) => {
  const productsPerPage = 8;

  const productsCount = await Product.countDocuments();

  const apiFeature = new ApiFeatures(Product.find(), req.query)
    .search()
    .filter();

  let products = await apiFeature.query;

  const filteredCount = products.length;

  apiFeature.pagination(productsPerPage);

  products = await apiFeature.query.clone();

  // products = await apiFeature.query;
  // console.log(productsPerPage, apiFeature.query);
  // const products = await Product.find().skip(10);

  res.status(200).json({
    success: true,
    products,
    productsCount,
    productsPerPage,
    filteredCount,
  });
});

